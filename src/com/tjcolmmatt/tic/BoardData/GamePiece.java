package com.tjcolmmatt.tic.BoardData;

// -------------------------------------------------------------------------
/**
 * An enumeration for possible game pieces on the board.
 *
 * @author Matthew Wong (mttwong)
 * @author Colm Gallagher (mecolmg)
 * @version Dec 3, 2014
 */
public enum GamePiece
{
    /**
     * An X on the board.
     */
    X,
    /**
     * An O on the board.
     */
    O,
    /**
     * An empty tile on the board.
     */
    BLANK,
    /**
     * An invalid tile on the board.
     */
    INVALID;
}
